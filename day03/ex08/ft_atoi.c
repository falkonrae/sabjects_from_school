int		ft_atoi(char *str)
{
	int		o;
	int		res;
	int		minus;

	o = 0;
	res = 0;
	minus = 0;
	while ((str[o] == ' ') || (str[o] == '+'))
	{
		o++;
	}
	if (str[o] == '-')
	{
		minus = 1;
		o++;
	}
	while ((str[o] >= '0') && (str[o] <= '9'))
	{
		res = 10 * res + str[o] - '0';
		o++;
	}
	if (minus == 1)
		res = -res;
	return (res);
}
